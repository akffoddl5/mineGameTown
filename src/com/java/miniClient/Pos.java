package com.java.miniClient;

public class Pos {
	int row;
	int col;
	
	
	
	public Pos(int row, int col) {
		super();
		this.row = row;
		this.col = col;
	}
	
	public Pos() {
		
	}
	
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public int getCol() {
		return col;
	}
	public void setCol(int col) {
		this.col = col;
	}
	
	

}
